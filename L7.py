# Tuple operations in Python

numbers = (95,98,97,97,97)
print(numbers.count(97))
print(numbers.count(95))
print(numbers.index(97))

person = "ram","shyam","abhi"
print(person)

# Set operations in Python

fruits = {'apple', 'banana', 'cherry', 'date','date', 'cherry'}
for fruit in fruits:
    print(fruit)

# Dictionary operations in Pytnumbers

marks = {"english":95, "chemistry":98}
print(marks["chemistry"])
marks["physics"] = 97
print(marks)
marks["physics"] = 99
print(marks)